        <h2 class="title">REGULAMIN ODWIEDZANIA KINA</h2>
        
                
        <div class="entry clearfix">
                
                        
            <p style="text-align: justify;" align="center"><span style="text-align: justify;">Kino Baida to nowoczesny kompleks kinowy wyposażony w najnowszy sprzęt technologiczny, system dźwięku DOLBY DIGITAL oraz system projekcji optycznej REAL 3D.<br>
				Administracja kina wyraża szczerą wdzięczność wszystkim odwiedzającym i prosi o przestrzeganie poniższych zasad w celu utrzymania komfortowych warunków oglądania i relaksu.<br>
				Podczas wizyty w kinie prosimy o okazywanie szacunku gościom i pracownikom kina.<br>
				Kupując bilet wstępu, odwiedzający automatycznie akceptuje niniejsze zasady i wyraża zgodę na warunki uczestnictwa określone poniżej.</span></p>
<p style="text-align: center;"><strong>Godziny otwarcia kina:</strong></p>
<p style="text-align: justify;">Kino rozpoczyna pracę 15 minut przed rozpoczęciem pierwszego seansu.<br>
	Kasa kina zamykana jest 15 minut po zakończeniu ostatniego seansu.</p>
<p style="text-align: center;"><strong>Sprzedaż biletów:</strong></p>
<p style="text-align: justify;">- Sprzedane bilety podlegają obowiązkowemu zwrotowi wyłącznie w przypadku odwołania seansu lub zamiany filmu.<br>
	- Termin zwrotu biletu (na prośbę widza) upływa 30 minut przed rozpoczęciem sesji.<br>
	- W przypadku spóźnienia na sesję bilety nie są akceptowane ani wymieniane w kasie.<br>
	- Zniżki i przywileje są udzielane zgodnie z polityką marketingową kina i obowiązują na filmy, na które właściciele praw autorskich zezwalają na zniżki.<br>
	- Godzina rozpoczęcia sesji wskazana na bilecie oznacza godzinę rozpoczęcia danego filmu, w tym wszystkich materiałów zapowiadających i promocyjnych.</p>
<p style="text-align: center;"><strong>Tryb przejścia:</strong></p>
<p style="text-align: justify;">- Zgłoszenie bez linii kontrolnej lub bez kodowania jest uznawane za nieważne.<br>
	- Należy zająć miejsca w audytorium zgodnie z numerem rzędu i miejsca wskazanym na bilecie.<br>
	- Widz zobowiązany jest zachować bilet do końca seansu. W przypadku zagubienia biletu, usługa seansu filmowego uznawana jest za nieopłaconą.<br>
	- Pracownik kina ma prawo zażądać od widza biletu na dodatkową kontrolę w dowolnym momencie podczas całego seansu.<br>
	- Widzowie poniżej 18 roku życia nie mogą uczestniczyć w pokazach filmów z ograniczeniami wiekowymi ustanowionymi przez Ministerstwo Kultury i Turystyki Ukrainy bez zgody rodziców lub osób sprawujących władzę rodzicielską. Przez osoby sprawujące władzę rodzicielską rozumie się: dorosłych bliskich krewnych (braci i siostry, dziadków, rodzeństwo rodziców), rodziców adopcyjnych, opiekunów.</p>
<p style="text-align: center;"><strong>Zakaz:</strong></p>
<p style="text-align: justify;">- Wejście do audytorium bez biletu.<br>
	- Zakłócanie oglądania filmu przez innych widzów.<br>
	- Wizyta w kinie pod wpływem alkoholu lub narkotyków; ze zwierzętami; ze sprzętem do samoobrony, przedmiotami do przekłuwania i cięcia, z jakimkolwiek rodzajem broni.<br>
	- Na terenie całego kompleksu kinowego obowiązuje zakaz palenia. Zakaz dotyczy wszystkich rodzajów wyrobów tytoniowych i elektronicznych analogów.<br>
	- Chodzenie do kina w brudnych ubraniach.<br>
	- zaśmiecanie i rozrzucanie różnych przedmiotów (opakowań, gum do żucia, serwetek itp.)<br>
	- Wnoszenia i spożywania jedzenia i napojów zakupionych poza kinem.<br>
	- Robienie zdjęć i nagrywanie filmów w audytorium oraz korzystanie ze sprzętu nagrywającego podczas projekcji.<br>
	- Tworzenie przeszkód w pracy pracowników kina.<br>
	- Używaj nieprzyzwoitego języka w swoich wypowiedziach.<br>
	- Goście mogą rozsiąść się wygodnie na sofach i fotelach w holu i kinie.<br>
	- Zmień lokalizację nieruchomości w hali i bufecie.</p>
<p style="text-align: center;"><strong>Odpowiedzialność stron:</strong></p>
<p style="text-align: justify;">- Odwiedzający kino są zobowiązani do dbania o mienie, zapobiegania jego uszkodzeniom, a także podejmowania innych działań, które zakłócają normalny przebieg wydarzeń, utrzymania czystości i porządku w sali kinowej, kawiarni i ogólnie na terenie kina.<br>
	- W przypadku uszkodzenia mienia kina odwiedzający zobowiązany jest do zwrotu pełnej kwoty szkody.<br>
	- W przypadku naruszenia ogólnie przyjętych zasad i norm zachowania (wulgarny język, agresywne zachowanie, zakłócanie ciszy na sali podczas seansu itp.) obsługa kina ma prawo usunąć takiego gościa z sali kinowej i stołówki. W takim przypadku cena biletu nie zostanie zwrócona.<br>
	- Wszyscy widzowie mają prawo do składania skarg dotyczących świadczenia usług przez kino do jego administracji w ciągu dnia roboczego. Książka skarg i zażaleń jest prowadzona przez kasjera kina i jest wydawana na pierwsze żądanie odwiedzających.</p>

